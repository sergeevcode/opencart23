class ControllerCheckoutCheckout extends Controller {
	public function index() {
		// Validate cart has products and has stock.
		if ((!$this->cart->hasProducts() && empty($this->session->data['vouchers'])) || (!$this->cart->hasStock() && !$this->config->get('config_stock_checkout'))) {
			$this->response->redirect($this->url->link('checkout/cart'));
		}

		// Validate minimum quantity requirements.
		$products = $this->cart->getProducts();

		foreach ($products as $product) {
			$product_total = 0;

			foreach ($products as $product_2) {
				if ($product_2['product_id'] == $product['product_id']) {
					$product_total += $product_2['quantity'];
				}
			}

			if ($product['minimum'] > $product_total) {
				$this->response->redirect($this->url->link('checkout/cart'));
			}
		}



		$data['deliveries'] = array();

		$filter_data = array(
			'filter_category_id' => '72'
		);
 		
		$this->load->model('catalog/product');

		$results = $this->model_catalog_product->getProducts($filter_data);

		foreach ($results as $result) {

			$data['deliveries'][] = array(
				'delivery_id'  => $result['product_id'],				
				'name'        => $result['name'], 
				'price'       => number_format($result['price'], 0, '', '')
			);
		} 

		$data['delivery_tax'] = $this->model_catalog_product->getProduct(58);
	 
		$filter_data = array(
			'filter_category_id' => '73'
		);
 		 
		$results = $this->model_catalog_product->getProducts($filter_data);

		$data['services'] = array();

		foreach ($results as $result) {
			if ($result['price'] > 0) {
				$price = $this->currency->format($result['price'], $this->session->data['currency']);
			} else {
				$price = "Бесплатно";
			}

			$data['services'][] = array(
				'service_id'  => $result['product_id'],				
				'name'        => $result['name'], 
				'price'       => $price,
				'price_number' => number_format($result['price'], 0, '', '')
			);
		} 
				$total_data = array(
			'totals' => &$totals,
			'taxes'  => &$taxes,
			'total'  => &$total
		);

		$this->load->model('extension/extension');

		$sort_order = array();

		$results = $this->model_extension_extension->getExtensions('total');

		foreach ($results as $key => $value) {
			$sort_order[$key] = $this->config->get($value['code'] . '_sort_order');
		}

		array_multisort($sort_order, SORT_ASC, $results);

		foreach ($results as $result) {
			if ($this->config->get($result['code'] . '_status')) {
				$this->load->model('extension/total/' . $result['code']);

				// We have to put the totals in an array so that they pass by reference.
				$this->{'model_extension_total_' . $result['code']}->getTotal($total_data);
			}
		}

		$sort_order = array();

		foreach ($totals as $key => $value) {
			$sort_order[$key] = $value['sort_order'];
		}

		array_multisort($sort_order, SORT_ASC, $totals);
		$total_summ = $totals[count($totals) - 1]; 
		$data['totals'] = $total_summ['value'];

		$this->load->language('checkout/checkout');

		$this->document->setTitle($this->language->get('heading_title'));

		// $this->document->addScript('catalog/view/javascript/jquery/datetimepicker/moment.js');
		// $this->document->addScript('catalog/view/javascript/jquery/datetimepicker/bootstrap-datetimepicker.min.js');
		// $this->document->addStyle('catalog/view/javascript/jquery/datetimepicker/bootstrap-datetimepicker.min.css');

		// Required by klarna
		// if ($this->config->get('klarna_account') || $this->config->get('klarna_invoice')) {
		// 	$this->document->addScript('http://cdn.klarna.com/public/kitt/toc/v1.0/js/klarna.terms.min.js');
		// }
		
		$data['locations'] = array();

		$this->load->model('localisation/location');

		foreach((array)$this->config->get('config_location') as $location_id) {
			$location_info = $this->model_localisation_location->getLocation($location_id);

			if ($location_info) {
				if ($location_info['image']) {
					$image = $this->model_tool_image->resize($location_info['image'], $this->config->get($this->config->get('config_theme') . '_image_location_width'), $this->config->get($this->config->get('config_theme') . '_image_location_height'));
				} else {
					$image = false;
				}

				$data['locations'][] = array(
					'location_id' => $location_info['location_id'],
					'name'        => $location_info['name'],
					'address'     => nl2br($location_info['address']),
					'geocode'     => $location_info['geocode'],
					'telephone'   => $location_info['telephone'],
					'telephone_publish' => $this->phone_number($location_info['telephone']),
